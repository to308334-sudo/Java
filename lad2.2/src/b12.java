import java.util.Scanner;

public class b12 {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int[] a=new int[n];
        for(int i=0;i<n;i++) a[i]=sc.nextInt();

        for(int x:a){
            int sum=0, t=x;
            while(t>0){ sum+=t%10; t/=10; }
            System.out.println(x+" -> "+sum);
        }
    }
}
